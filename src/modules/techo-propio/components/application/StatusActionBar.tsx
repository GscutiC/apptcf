/**
 * StatusActionBar - Barra de acciones contextual según el estado de la solicitud
 * Muestra botones de acción disponibles según el estado actual
 */

import React from 'react';
import { TechoPropioApplication, ApplicationStatus } from '../../types';
import { Button } from '../common';
import { ALLOWED_STATUS_TRANSITIONS } from '../../utils';

interface StatusActionBarProps {
  application: TechoPropioApplication;
  onEdit?: () => void;
  onSubmit?: () => void;
  onStartReview?: () => void;
  onApprove?: () => void;
  onReject?: () => void;
  onRequestInfo?: () => void;
  onCancel?: () => void;
  onReactivate?: () => void;
  onDelete?: () => void;
  onPrint?: () => void;
  disabled?: boolean;
}

export const StatusActionBar: React.FC<StatusActionBarProps> = ({
  application,
  onEdit,
  onSubmit,
  onStartReview,
  onApprove,
  onReject,
  onRequestInfo,
  onCancel,
  onReactivate,
  onDelete,
  onPrint,
  disabled = false
}) => {
  // Verificar que el ID existe
  if (!application.id) {
    return (
      <div className="flex gap-2">
        <span className="text-sm text-gray-400 italic">Sin ID - No hay acciones disponibles</span>
      </div>
    );
  }

  // ✅ VALIDACIÓN: Función para verificar transiciones de estado permitidas
  const isTransitionAllowed = (targetStatus: ApplicationStatus): boolean => {
    const allowedTransitions = ALLOWED_STATUS_TRANSITIONS[application.status] || [];
    return allowedTransitions.includes(targetStatus);
  };

  // Renderizar botones según el estado
  const renderActions = () => {
    switch (application.status) {
      case ApplicationStatus.DRAFT:
        return (
          <>
            <Button
              onClick={onEdit}
              variant="secondary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 5H6a2 2 0 00-2 2v11a2 2 0 002 2h11a2 2 0 002-2v-5m-1.414-9.414a2 2 0 112.828 2.828L11.828 15H9v-2.828l8.586-8.586z" />
                </svg>
              }
            >
              Editar
            </Button>
            <Button
              onClick={onSubmit}
              variant="primary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 19l9 2-9-18-9 18 9-2zm0 0v-8" />
                </svg>
              }
            >
              Enviar
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      case ApplicationStatus.SUBMITTED:
        return (
          <>
            <Button
              onClick={onStartReview}
              variant="primary"
              disabled={disabled || !isTransitionAllowed(ApplicationStatus.UNDER_REVIEW)}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z" />
                </svg>
              }
            >
              Iniciar Revisión
            </Button>
            <Button
              onClick={onEdit}
              variant="secondary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 5H6a2 2 0 00-2 2v11a2 2 0 002 2h11a2 2 0 002-2v-5m-1.414-9.414a2 2 0 112.828 2.828L11.828 15H9v-2.828l8.586-8.586z" />
                </svg>
              }
            >
              Editar
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      case ApplicationStatus.UNDER_REVIEW:
        return (
          <>
            <Button
              onClick={onApprove}
              variant="success"
              disabled={disabled || !isTransitionAllowed(ApplicationStatus.APPROVED)}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M5 13l4 4L19 7" />
                </svg>
              }
            >
              Aprobar
            </Button>
            <Button
              onClick={onReject}
              variant="danger"
              disabled={disabled || !isTransitionAllowed(ApplicationStatus.REJECTED)}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
                </svg>
              }
            >
              Rechazar
            </Button>
            <Button
              onClick={onRequestInfo}
              variant="primary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9 12h6m-6 4h6m2 5H7a2 2 0 01-2-2V5a2 2 0 012-2h5.586a1 1 0 01.707.293l5.414 5.414a1 1 0 01.293.707V19a2 2 0 01-2 2z" />
                </svg>
              }
            >
              Solicitar Info
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      case ApplicationStatus.ADDITIONAL_INFO_REQUIRED:
        return (
          <>
            <Button
              onClick={onEdit}
              variant="primary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M11 5H6a2 2 0 00-2 2v11a2 2 0 002 2h11a2 2 0 002-2v-5m-1.414-9.414a2 2 0 112.828 2.828L11.828 15H9v-2.828l8.586-8.586z" />
                </svg>
              }
            >
              Editar y Completar
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      case ApplicationStatus.APPROVED:
        return (
          <>
            <Button
              onClick={onReject}
              variant="danger"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M6 18L18 6M6 6l12 12" />
                </svg>
              }
            >
              Revertir Aprobación
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      case ApplicationStatus.REJECTED:
        return (
          <>
            <Button
              onClick={onReactivate}
              variant="primary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 4v5h.582m15.356 2A8.001 8.001 0 004.582 9m0 0H9m11 11v-5h-.581m0 0a8.003 8.003 0 01-15.357-2m15.357 2H15" />
                </svg>
              }
            >
              Reconsiderar
            </Button>
            <Button
              onClick={onDelete}
              variant="danger"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
                </svg>
              }
            >
              Eliminar Definitivamente
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      case ApplicationStatus.CANCELLED:
        return (
          <>
            <Button
              onClick={onReactivate}
              variant="primary"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 4v5h.582m15.356 2A8.001 8.001 0 004.582 9m0 0H9m11 11v-5h-.581m0 0a8.003 8.003 0 01-15.357-2m15.357 2H15" />
                </svg>
              }
            >
              Reactivar Solicitud
            </Button>
            <Button
              onClick={onDelete}
              variant="danger"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 7l-.867 12.142A2 2 0 0116.138 21H7.862a2 2 0 01-1.995-1.858L5 7m5 4v6m4-6v6m1-10V4a1 1 0 00-1-1h-4a1 1 0 00-1 1v3M4 7h16" />
                </svg>
              }
            >
              Eliminar Definitivamente
            </Button>
            <Button
              onClick={onPrint}
              variant="ghost"
              disabled={disabled}
              leftIcon={
                <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
                </svg>
              }
            >
              Imprimir
            </Button>
          </>
        );

      default:
        return (
          <Button
            onClick={onPrint}
            variant="ghost"
            disabled={disabled}
            leftIcon={
              <svg className="w-4 h-4" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 17h2a2 2 0 002-2v-4a2 2 0 00-2-2H5a2 2 0 00-2 2v4a2 2 0 002 2h2m2 4h6a2 2 0 002-2v-4a2 2 0 00-2-2H9a2 2 0 00-2 2v4a2 2 0 002 2zm8-12V5a2 2 0 00-2-2H9a2 2 0 00-2 2v4h10z" />
              </svg>
            }
          >
            Imprimir
          </Button>
        );
    }
  };

  return (
    <div className="flex flex-wrap gap-2">
      {renderActions()}
    </div>
  );
};

export default StatusActionBar;
